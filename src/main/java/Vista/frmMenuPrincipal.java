    private void btnUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUsuariosActionPerformed
        frmRegistroUsuarios frmR = new frmRegistroUsuarios();
        if (lblTipoUsuario.getText().equals("EMPLEADO") || lblTipoUsuario.getText().equals("CLIENTE")) {
            frmR.setVisible(false);
            JOptionPane.showMessageDialog(this, "Solo los Administradores pueden ingresar al registro de usuarios");
        } else if  (lblTipoUsuario.getText().equals("ADMINISTRADOR")){
            jdesktopPane.add(frmR);
            ControladorUsuarios control = new ControladorUsuarios(frmR);
            control.inicio();
            frmR.toFront();
            frmR.setVisible(true);
            frmR.setLocation(jdesktopPane.getWidth() / 2 - frmR.getWidth() / 2, jdesktopPane.getHeight() / 2 - frmR.getHeight() / 2);
        }
    }//GEN-LAST:event_btnUsuariosActionPerformed

