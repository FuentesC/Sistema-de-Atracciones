    private void btnSeguimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguimientoActionPerformed
        frmSeguimiento frmS = new frmSeguimiento();
        if (lblTipoUsuario.getText().equals("CLIENTE")) {
            frmS.setVisible(false);
            JOptionPane.showMessageDialog(this, "Solo Administradores o Empleados pueden ingresar a esta ventana");
        } else if (lblTipoUsuario.getText().equals("ADMINISTRADOR") || lblTipoUsuario.getText().equals("EMPLEADO")) {
            ControladorSeguimiento con = new ControladorSeguimiento(frmS);
            con.inicio();
            con.inicio2();
            jdesktopPane.add(frmS);
            frmS.toFront();
            frmS.setVisible(true);
            frmS.setLocation(jdesktopPane.getWidth() / 2 - frmS.getWidth() / 2, jdesktopPane.getHeight() / 2 - frmS.getHeight() / 2);
        }
        
    }//GEN-LAST:event_btnSeguimientoActionPerformed

